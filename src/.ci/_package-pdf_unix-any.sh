. "${LIBS_AUTOMATACI}/services/io/fs.sh"
. "${LIBS_AUTOMATACI}/services/i18n/translations.sh"




# initialize
if [ "$PROJECT_PATH_ROOT" = "" ]; then
        >&2 printf "[ ERROR ] - Please run from automataCI/ci.sh.ps1 instead!\n"
        return 1
fi




PACKAGE_Assemble_PDF_Content() {
        _target="$1"
        _directory="$2"
        _target_name="$3"
        _target_os="$4"
        _target_arch="$5"


        # validate project
        if [ $(FS_Is_Target_A_PDF "$_target") -ne 0 ]; then
                return 10 # not applicable
        fi


        # execute
        __dest="${_directory}/$(FS_Get_File "$_target")"
        I18N_Copy "$_target" "$__dest"
        FS_Copy_File "$_target" "$__dest"
        if [ $? -ne 0 ]; then
                I18N_Copy_Failed
                return 1
        fi


        # report status
        return 0
}
